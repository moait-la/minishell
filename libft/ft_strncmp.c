#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	long	res;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
		{
			res = ((unsigned char)s1[i] - (unsigned char)s2[i]);
			if (res > 0)
				return (1);
			else
				return (-1);
		}
		i++;
	}
	return (0);
}
